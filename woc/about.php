<p>The hobby of collecting includes seeking, locating, acquiring, organizing, cataloging, displaying, storing, and maintaining whatever items are of interest to the individual collector. The scope of collecting is unlimited, which is expressed in the hyperbolic expression: "If something exists, somebody somewhere collects them.The most obvious way to categorise collections is by the type of objects collected. Most collections are of manufactured commercial items, but natural objects such as birds' eggs, butterflies, rocks, and seashells can also be the subject of a collection. Some collectors may collect not a specific type of object, but instead objects with specific properties; for example, some people collect items that once belonged to famous people. Among collections of manufactured items, the objects may be antique, or simply collectible. Antiques are collectible items at least 100 years old, while collectibles can be arbitrarily recent. Collectors and dealers may use the word vintage to describe older collectibles. Items which were once everyday objects but may now be collectible since almost all those once produced have been destroyed or discarded are called ephemera. Philately, phillumeny, and deltiology (collecting postage stamps, matchboxes, and postcards) are forms of collecting which can be undertaken at minimal expense. </br></p>







